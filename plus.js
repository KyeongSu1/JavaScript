function plus(num1,num2){
    let result;  /* num1+num2 = 값의 저장소 만드는것 */
    result=num1+num2; /* result <- 임의저장소에 저장해라 */
    return result /*result의 들어있는 값을 돌려줘라(return) */
}
function minus(num1,num2){
    let result;  
    result=num1-num2; 
    return result 
}
function division(num1,num2){
    let result;  
    result=num1/num2; 
    return result 
}
function multiplication(num1,num2){
    let result;  
    result=num1*num2; 
    return result 
}